#include<iostream>
#include<stdlib.h>
using namespace std;
//求Fibonacci级数
unsigned long long myFibonacci(unsigned long long n){
    //cout<<n<<ends;//打印调试信息
    if(n==1 || n==2) return 1;
    else return myFibonacci(n-1)+myFibonacci(n-2);
}

int main()
{
    //输入
    unsigned long long n;
    cout<<"请输入一个整数n: "<<endl;
    cin>>n;
    //输出Fibonacci级数的值
    cout<<"F(n): "<<myFibonacci(n)<<endl;
    
    system("pause");
    return 0;
}
